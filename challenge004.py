
# Challenge 004 - Examining a value

v1 = input('Type something: ')
print('Primitive type: ', type(v1))
print('is numeric? ', v1.isnumeric())
print('is alnum? ', v1.isalnum())
print('is alpha? ', v1.isalpha())
print('is ascii? ', v1.isascii())
print('is digit? ', v1.isdigit())
print('is decimal? ', v1.isdecimal())
print('is identifier? ', v1.isidentifier())
print('is lower? ', v1.islower())
print('is upper? ', v1.isupper())
print('is printable? ', v1.isprintable())
print('is space? ', v1.isspace())
print('is title? ', v1.istitle())